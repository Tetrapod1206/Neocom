#import "NCCacheRecord.h"
#import "NCCache.h"

@implementation NCCacheRecord

@dynamic recordID;
@dynamic data;
@dynamic date;
@dynamic expireDate;
@dynamic section;

- (BOOL) isExpired {
	return !self.date || !self.expireDate || [self.expireDate compare:[NSDate date]] == NSOrderedAscending;
}

@end
